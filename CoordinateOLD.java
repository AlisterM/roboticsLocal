package navigateBot;

public class CoordinateOLD {
	
	private int x;
	private int y;
	
	public CoordinateOLD(int x, int y) {
	    this.x = x;
	    this.y = y;
	  }

	  public int getX(){
		  return this.x;
	  }
	  
	  public void setX(int NX){
		  this.x = NX;
	  }
	  
	  public int getY(){
		  return this.y;
	  }

	  public void setY(int NY){
		  this.y = NY;
	  }

}
